void lcd_init(void) {
    /* Set dimensions for 320x240 */
    CLCD_TIM0 = ( (320/16 - 1) << 2 ); 
    CLCD_TIM1 = ( 240 - 1 );
    
    /* Set Framebuffer address */
    CLCD_UPBASE = LCD_FB_ADDR;
    
    /* 16-bit color, Power on, Enable */
    CLCD_CONTROL = CLCD_CTRL_EN | CLCD_CTRL_BPP16 | CLCD_CTRL_PWR;
}

void lcd_clear(uint16_t color) {
    for (int i = 0; i < LCD_WIDTH * LCD_HEIGHT; i++) {
        LCD_FB[i] = color;
    }
}

void lcd_draw_pixel(int x, int y, uint16_t color) {
    if (x >= 0 && x < LCD_WIDTH && y >= 0 && y < LCD_HEIGHT) {
        LCD_FB[y * LCD_WIDTH + x] = color;
    }
}

void lcd_draw_rect(int x, int y, int w, int h, uint16_t color) {
    for (int j = y; j < y + h; j++) {
        for (int i = x; i < x + w; i++) {
            lcd_draw_pixel(i, j, color);
        }
    }
}

void lcd_draw_char(int x, int y, char c, uint16_t color, uint16_t bg) {
    if (c < 32 || c > 126) return;
    const uint8_t *bitmap = font8x8[c - 32];
    for (int j = 0; j < 8; j++) {
        for (int i = 0; i < 8; i++) {
            if (bitmap[j] & (1 << (7 - i))) {
                lcd_draw_pixel(x + i, y + j, color);
            } else if (bg != 0) {
                lcd_draw_pixel(x + i, y + j, bg);
            }
        }
    }
}

void lcd_draw_string(int x, int y, const char *str, uint16_t color, uint16_t bg) {
    while (*str) {
        lcd_draw_char(x, y, *str++, color, bg);
        x += 8;
    }
}
